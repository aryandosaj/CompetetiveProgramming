ll n, m, k;
ll rec(ll s, ll e)
{
    if (dp[s][e] != -1)
        return dp[s][e];
    if (s == e)
        return arr[s];
    ll l = e - s + 1;
    if (l == (n - m + 1))
        return max(arr[s], arr[e]);
    if (l >= (n - k + 1))
        return dp[s][e] = max(rec(s + 1, e), rec(s, e - 1));
    
    return dp[s][e] = min(rec(s + 1, e), rec(s, e - 1));
}
